package xy.yangtzeu.exception;

public class BusinessException extends RuntimeException{

	private static final long serialVersionUID = -4524660362238996497L;

	public BusinessException(){
		super();
	}
	
	public BusinessException(String argo , Throwable arg1){
		super(argo ,arg1);
	}
	
	public BusinessException(String argo){
		super(argo);
	}
	
	public BusinessException(Throwable arg1){
		super(arg1);
	}
}
